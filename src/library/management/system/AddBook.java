    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                if(txtid.getText().equals("")){
                JOptionPane.showMessageDialog(this, "   all fild is required");// TODO add your handling code here:
                txtid.requestFocus();
    }
                else if(txtname.getText().equals("")){
                    JOptionPane.showMessageDialog(this, "all fild is required");
                    txtname.requestFocus();
                }
                 else if(txtPublisher.getText().equals("")){
                    JOptionPane.showMessageDialog(this, "all fild is required");
                    txtPublisher.requestFocus();
                }
                 else if(txtprice.getText().equals("")){
                    JOptionPane.showMessageDialog(this, "all fild is required");
                    txtprice.requestFocus();
                }
                 else if(txtyear.getText().equals("")){
                    JOptionPane.showMessageDialog(this, "all fild is required");
                    txtyear.requestFocus();
                }
                 else{
                     Connection con=Connect.getConnection();
                     
                    try {
                        PreparedStatement ps=con.prepareStatement("insert into book(Id, Book_name, Publisher, Price, Year, Status) values(?,?,?,?,?,?)");
                    ps.setString(1,txtid.getText());
                    ps.setString(2,txtname.getText());
                    ps.setString(3,txtPublisher.getText());
                    ps.setString(4,txtprice.getText());
                    ps.setString(5,txtyear.getText());
                    ps.setString(6,"NOT-ISSUED");
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Record Saved");
                    txtid.setText("");
                    txtname.setText("");
                    txtPublisher.setText("");
                    txtprice.setText("");
                    txtyear.setText("");
                    } catch (SQLException ex) {
                        Logger.getLogger(AddBook.class.getName()).log(Level.SEVERE, null, ex);
                    }
                 }
                
    }//GEN-LAST:event_jButton1ActionPerformed

