    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
           if(txtstudentid.getText().equals("")){
               JOptionPane.showMessageDialog(this,"Please Enter Student id and Search");
                  txtstudentid.requestFocus();
           }
           else{
               try {
                   pst = con.prepareStatement("SELECT student.Id, book.Id, book.Book_name,student.Name, book.Issue_date, book.due_date from student inner join book on student.Id = book.student_Id where book.Id=?");
                        pst.setString(1, txtstudentid.getText());
                        
                    rs=pst.executeQuery();
                    if(rs.next()){
                        
                        txtbookid.setText(rs.getString(2));
                        txtbookname.setText(rs.getString(3));
                        txtstudentname.setText(rs.getString(4));
                        txtissuedate.setText(rs.getString(5));
                        txtduedate.setText(rs.getString(6));
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "Please enter valid student id");
                    }      
               } catch (SQLException ex) {
                   Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
               }
           }
    }//GEN-LAST:event_jButton3ActionPerformed

